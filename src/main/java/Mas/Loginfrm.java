    private void validar() {
        String usuario = txtUser.getText();
        String contraseña = new String(txtPass.getPassword()); // Usamos getPassword() para obtener la contraseña

        // Validar usuario y contraseña
        if ("Miranda".equals(usuario) && "1234".equals(contraseña)) {
            JOptionPane.showMessageDialog(this, "Bienvenido a nuestro sistema....");
            // Aquí puedes redirigir al formulario principal
            new principal().setVisible(true); // Asegúrate de que MainForm es el nombre de tu formulario principal
            this.dispose(); // Cierra el formulario de login
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


